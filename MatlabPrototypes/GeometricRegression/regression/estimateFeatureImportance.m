% ========== Estimating Feature Importance
disp('Estimating feature importance...');

regressor = TreeBagger(regressionInfo.nbOfTrees,...
    regressionInfo.trainingFeatures,regressionInfo.trainingLabels,...
    'Method','R',...
    'OOBVarImp','On',...
    'MinLeaf',regressionInfo.nbOfLeaves);

regressionInfo.featuresImportance = regressor.OOBPermutedVarDeltaError;
